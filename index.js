function add(a, b) {
  return a+b
}

function subtract(a, b) {
  return a-b
}

function multiply(a, b){
  return a*b
}

function divide(a, b) {
  return a/b
}

function increment(n){
  return ++n
}

function decrement(n){
  return --n
}

function makeInt(str){
  return parseInt(str, 10)
}

function preserveDecimal(str){
  return parseFloat(str)
}